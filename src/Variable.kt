fun main(args: Array<String>) {

    // variable implicitly with value auto assigned by datatype
    var job = "Android App Developer"
    val salary = 20000
    println("My Job Designation is: "+job+" & Salary: "+salary)


    // variable explicitly with value (Not Mendatory)
    var job1: String = "Android App Developer"
    val salary1: Int = 20000
    println("My Job Designation is: "+job1+" & Salary: "+salary1)


    var job2: String
    val salary2: Int

    job2 = "Software Engineer"
    salary2 = 30000
    println("My Job Designation is: "+job2+" & Salary: "+salary2)





}